# alias for git-extras
# https://github.com/tj/git-extras/blob/master/Commands.md

alias gbc='git create-branch'
alias gbcr='git create-branch -r'
# gbd is provided by zsh git plugin
alias gbdr='git delete-branch'
alias gsq='git squash'
# squash last 5 commits
#  $ gsq HEAD~5 'some message'
# squash src_branch to current branch
#  $ gsq src_branch 'some message'
